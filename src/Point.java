
public class Point {

	private double x;
	private double y;
	private int tag;
	
	public Point(double x, double y, int tag){
		this.x = x;
		this.y = y;
		this.tag = tag;
	}
	
	public double getX(){
		return this.x;
	}
	
	public double getY(){
		return this.y;
	}
	
	public int getTag(){
		return this.tag;
	}
	
		
}
